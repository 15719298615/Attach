package com.attach.group.commons.pojo;

public class members {
    private String member_name;
    private Integer member_id;
    private Double member_task_rate;

    public String getMember_name() {
        return member_name;
    }

    public void setMember_name(String member_name) {
        this.member_name = member_name;
    }

    public Integer getMember_id() {
        return member_id;
    }

    public void setMember_id(Integer member_id) {
        this.member_id = member_id;
    }

    public Double getMember_task_rate() {
        return member_task_rate;
    }

    public void setMember_task_rate(Double member_task_rate) {
        this.member_task_rate = member_task_rate;
    }
}
